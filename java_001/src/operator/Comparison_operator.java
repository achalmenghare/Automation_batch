package operator;

public class Comparison_operator {

	public static void main(String[] args) {
		
	int u= 45;
	int n = 78;
	
	boolean m = u<n;
	System.out.println(m);
	
	boolean l = u>n;
	System.out.println(l);
	
	boolean o = u<=n;
	System.out.println(o);
	
	boolean q = u!=n;
	System.out.println(q);
	
	boolean i = u<n;
	System.out.println(i);
	
	System.out.println("-----------------------------------");
	int i1 = 300;
	long k1 = 900;
	
	boolean a_1 = k1>i1;
	System.out.println(a_1);
	
	boolean a_2= k1!=i1;
	System.out.println(a_2);
	}
	
	}


