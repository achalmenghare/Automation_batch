package java_control_statement;

public class if_else_statement {

	public static void main(String[] args) {
		byte j = 30;
		byte k = 40;
		if (j > k) {
			System.out.println("j is less than k");
		} else {
			System.out.println("j is greater than k");
		}
		System.out.println("--------------------------------");

		if (k > 80) {
			System.out.println("k is greater than 80");

		} else {
			System.out.println("above condition is false");
			System.out.println("------------------------");

		}

		float l = 85.63f;
		if (l == 85.63f) {
			System.out.println(l);
		} else {
			System.out.println("specified condition is true");
			System.out.println("--------------------------");
		}
		
		

		}

	}

