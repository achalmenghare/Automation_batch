package loop;

public class For_loop {
	public static void main(String[] args) {
		// 1 to 50
		for (int m = 1; m < 51; ++m) {
			System.out.println(m);

		}
		System.out.println("-----------------------------------------------------------------------");

		// 25 to 1
		for (int n = 25; n > 0; --n) {
			System.out.println(n);

		}
		System.out.println("----------------------------------------------------------------------");

		// 15 to 20
		for (int p = 15; p <= 20; ++p) {
			System.out.println(p);
		}
		System.out.println("---------------------------------------------------------------------------");

		// 100 to 50
		for (int q = 100; q > 50; --q) {
			System.out.println(q);
		}
		System.out.println("----------------------------------------------------------------------------");
		
		//1 to 10
		for(int a = 1;a<=10;++a){
		System.out.println(a);	
			
			
		}
	}

}
