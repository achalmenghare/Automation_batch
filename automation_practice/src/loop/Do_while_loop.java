package loop;

public class Do_while_loop {

	public static void main(String[] args) {

		int a = 44;
		do {
			if (a % 2 == 0) {
				System.out.println(a + " ");

			}
			a++;

		} while (a <=60);

		
		int b = 18;
		do {
			if (b % 2 != 0) {
				System.out.println(b + " ");

			}
			b++;

		} while (b <=30);

		
		
		int t = 56;
		do {
			if (t % 2 != 0) {
				System.out.println(t + " ");

			}
			t++;

		} while (t <=70);

	
	
	
	
	}

}
