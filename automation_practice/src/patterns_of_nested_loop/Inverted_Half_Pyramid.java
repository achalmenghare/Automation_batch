package patterns_of_nested_loop;

public class Inverted_Half_Pyramid {

	public static void main(String[] args) {
	// inverted half pyramid
    // row - 5
	//col - 5
		
	for(int m = 5;m>=1;--m) {
	for(int n = 1;n<=m;++n)	{
	System.out.print("* ");	
	}
	System.out.println();	
		
		
	}	
		
	
	
	
	
	
	
	}

}
