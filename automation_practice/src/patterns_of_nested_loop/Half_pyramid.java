package patterns_of_nested_loop;

public class Half_pyramid {

	
	public static void main(String[] args) {
		
		//half pyramid
		//row - 4
		//col - 4
			
		for(int x = 1;x<=4;++x)	{
		for(int y = 1;y<=x;++y)	{
		System.out.print("* ");	
			
			
		}
		System.out.println();	
			
			
		}
		
		
			
		}
}
